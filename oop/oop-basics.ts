/**
 * OOP란 객체 지향 프로그래밍으로
 * 관련 있는 데이터나 함수를 객체로 묶어서 사용하는 것을 의미한다.
 * 유지보수성, 재사용성, 확장성 등이 높다.
 * CLASS에 DATA 넣음 -> OBJECT(인스턴스) 생성
 * 
 * 클래스를 만들고 인스턴스를 만드는 게 객체지향은 아님
 * 객체지향 4가지 원칙(중요!!!)
 * 1. 캡슐화
 * 2. 추상화
 * 3. 상속
 * 4. 다형성
 * 
 * 나중에 정리 필요..
 * 
 * 캡슐화: 관련이 있는 데이터와 함수를 객체에 담아놓고 외부에서 내부를 볼 수 없도록 하는 것
 * 단 INPUT 값에 따라 내부의 STATE 변경
 * 
 * 추상화: 외부에 제공되는 Interface만 가지고 객체를 사용함
 * 
 * 상속: 객체를 다른 객체에서 재사용
 * 
 * 다형성: 하나의 객체가 상황에 따라 다른 의미로 해석 될 수 있음을 의미
 */